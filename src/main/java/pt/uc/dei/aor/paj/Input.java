package pt.uc.dei.aor.paj;

public class Input {
	
	private String tipo;
	private String conteudo;
	
	public Input(String tipo, String conteudo) {
		this.tipo = tipo;
		this.conteudo = conteudo;
	}

	public String getTipo() {
		return tipo;
	}

	public String getConteudo() {
		return conteudo;
	}
	
	
}
